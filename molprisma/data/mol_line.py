from enum import Enum, auto

import molprisma as mp

# //////////////////////////////////////////////////////////////////////////////
class MolKind(Enum):
    NONE = auto()
    META = auto()
    ATOM = auto()
    HETE = auto()


# //////////////////////////////////////////////////////////////////////////////
class MolLine:
    def __init__(self, text: str, kind: "mp.MolKind"):
        self.text = text
        self.kind = kind


